//WAP to print size of an int,a float ,a char and a double type variable.
#include<stdio.h>
int main()
{
    printf("The size of int is %d bytes.\n",sizeof(int));
    printf("The size of float is %d bytes.\n",sizeof(float));
    printf("The size of char is %d bytes.\n",sizeof(char));
    printf("The size of double is %d bytes.",sizeof(double));
    return 0;
}